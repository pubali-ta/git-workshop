# Custom python functions

def double_number(a):
    print(f'value before double_number(): {a}')
    b = a+a
    print(f'value after double_number(): {b}')
    return b


def square_number(a):
    print(f'value before square_number(): {a}')
    b = a*a
    print(f'value after square_number(): {b}')
    return b
